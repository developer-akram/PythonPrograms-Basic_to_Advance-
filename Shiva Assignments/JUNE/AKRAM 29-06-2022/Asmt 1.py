# 1. Write a program to print 1 to 10 and also 10 to 1
i = 1
print("Print 1 to 10 : ")
while(i <= 10):
    print(i)
    i += 1

print("Print 1 to 10 in same line : ")
i = 1
while i <= 10 :
    print(i,end=" ")
    i += 1

print("\nPrint 10 to 1 : ")
i = 10
while i > 0:
    print(i)
    i -= 1

